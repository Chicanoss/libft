#include "libft.h"
#include <string.h>

void	ft_bzero(void	*str, size_t	n)
{
	size_t	i;
	char	*j;

	i = 0;
	j = (char *) str;
	while (i < n)
	{	
		j[i] = 0;
		i++;
	}		
}
